package org.example.enums;

/**
 * Enum representing the height of the heels on a pair of shoes.
 */
public enum HeelHeight {
    /**
     * The shoes have flat heels.
     */
    FLAT,
    /**
     * The shoes have low heels.
     */
    LOW,
    /**
     * The shoes have high heels.
     */
    HIGH,
}
