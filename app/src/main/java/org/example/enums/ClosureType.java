package org.example.enums;

/**
 * Enum representing the type of closure used in a pair of shoes.
 */
public enum ClosureType {
    /**
     * The shoes have laces.
     */
    LACES,
    /**
     * The shoes use velcro.
     */
    VELCRO,
    /**
     * The shoes are slip-on.
     */
    SLIP_ON,
}
